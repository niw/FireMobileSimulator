	/**
	 * 絵文字のKDDI公式SJISコードと比較して、bit列が絵文字かどうかを判定する
	 * 
	 * @param {}
	 *            chs
	 * @return {}
	 */
	isPictogramSJISDecs : function(chs) {
		var result;
		if (chs.length != 2) {
			return false;
		}

		[char1, char2] = chs;
		if (((char1 == 0xF3 || char1 == 0xF6 || char1 == 0xF7) && ((char2 >= 0x40 && char2 <= 0x7E) || (char2 >= 0x80 && char2 <= 0xFC)))
				|| (char1 == 0xF4 && ((char2 >= 0x40 && char2 <= 0x7E) || (char2 >= 0x80 && char2 <= 0x8D)))) {
			result = true;
		} else {
			result = false;
		}
		return result;
	},

